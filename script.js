const mojibakePattern = /[\u00C2\u00C3]|\u00E2\u20AC/;

function repairMojibake(value) {
  if (typeof value !== "string" || !mojibakePattern.test(value)) {
    return value;
  }

  try {
    return decodeURIComponent(escape(value));
  } catch (error) {
    return value
      .replace(/\u00E2\u20AC\u2122/g, "'")
      .replace(/\u00E2\u20AC(?:\u0153|\u009D)/g, '"')
      .replace(/\u00E2\u20AC(?:\u201C|\u201D)/g, "-")
      .replace(/\u00E2\u20AC\u00A6/g, "...");
  }
}

function repairNestedStrings(value) {
  if (typeof value === "string") {
    return repairMojibake(value);
  }

  if (Array.isArray(value)) {
    value.forEach((item, index) => {
      value[index] = repairNestedStrings(item);
    });
    return value;
  }

  if (value && typeof value === "object") {
    Object.keys(value).forEach((key) => {
      value[key] = repairNestedStrings(value[key]);
    });
  }

  return value;
}

function repairObjectKeys(source) {
  return Object.fromEntries(
    Object.entries(source).map(([key, value]) => [repairMojibake(key), repairNestedStrings(value)])
  );
}

