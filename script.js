function initMenu() {
  const menuToggle = document.querySelector(".menu-toggle");
  const nav = document.querySelector(".site-nav");
  const headerShell = document.querySelector(".header-shell");
  const navLinks = document.querySelectorAll(".site-nav a");

  if (!menuToggle || !nav) {
    return;
  }

  menuToggle.setAttribute("aria-expanded", "false");

  const closeMenu = () => {
    document.body.classList.remove("nav-open");
    menuToggle.setAttribute("aria-label", uiMessages[currentLanguage].menuOpen);
    menuToggle.setAttribute("aria-expanded", "false");
  };

  const openMenu = () => {
    document.body.classList.add("nav-open");
    menuToggle.setAttribute("aria-label", uiMessages[currentLanguage].menuClose);
    menuToggle.setAttribute("aria-expanded", "true");
  };

  menuToggle.addEventListener("click", () => {
    if (document.body.classList.contains("nav-open")) {
      closeMenu();
    } else {
      openMenu();
    }
  });

  navLinks.forEach((link) => {
    link.addEventListener("click", closeMenu);
  });

  document.addEventListener("click", (event) => {
    if (window.innerWidth > 920 || !document.body.classList.contains("nav-open")) {
      return;
    }

    const target = event.target;
    if (!(target instanceof Node)) {
      return;
    }

    if (headerShell?.contains(target) || nav.contains(target)) {
      return;
    }

    closeMenu();
  });

  window.addEventListener("resize", () => {
    if (window.innerWidth > 920) {
      closeMenu();
    }
  });

  document.addEventListener("keydown", (event) => {
    if (event.key === "Escape") {
      closeMenu();
    }
  });
}

function initReveals() {
  const revealElements = document.querySelectorAll(".reveal");
  if (!revealElements.length) {
    return;
  }

  document.body.classList.add("ready");

  if (!("IntersectionObserver" in window)) {
    revealElements.forEach((element) => element.classList.add("is-visible"));
    return;
  }

  const observer = new IntersectionObserver(
    (entries) => {
      entries.forEach((entry) => {
        if (entry.isIntersecting) {
          entry.target.classList.add("is-visible");
          observer.unobserve(entry.target);
        }
      });
    },
    {
      threshold: 0.18,
      rootMargin: "0px 0px -40px 0px"
    }
  );

  revealElements.forEach((element) => observer.observe(element));
}

function initProjectCarousels() {
  const galleries = document.querySelectorAll(".project-gallery");
  if (!galleries.length) {
    return;
  }

  galleries.forEach((gallery) => {
    if (gallery.closest(".project-carousel")) {
      return;
    }

    const slides = Array.from(gallery.querySelectorAll(".project-gallery-item"));
    if (!slides.length) {
      return;
    }

    const wrapper = document.createElement("div");
    wrapper.className = "project-carousel";

    const viewport = document.createElement("div");
    viewport.className = "project-carousel-viewport";
    viewport.tabIndex = 0;
    viewport.setAttribute("aria-label", "Galeria de imagens do projeto");

    const prevButton = document.createElement("button");
    prevButton.type = "button";
    prevButton.className = "project-carousel-button prev";
    prevButton.setAttribute("aria-label", "Imagem anterior");
    prevButton.innerHTML = "&#10094;";

    const nextButton = document.createElement("button");
    nextButton.type = "button";
    nextButton.className = "project-carousel-button next";
    nextButton.setAttribute("aria-label", "Imagem seguinte");
    nextButton.innerHTML = "&#10095;";

    const parent = gallery.parentNode;
    if (!(parent instanceof Node)) {
      return;
    }

    parent.insertBefore(wrapper, gallery);
    wrapper.appendChild(prevButton);
    wrapper.appendChild(viewport);
    viewport.appendChild(gallery);
    wrapper.appendChild(nextButton);

    let currentIndex = 0;
    const maxIndex = slides.length - 1;

    const updateCarousel = () => {
      gallery.style.transform = `translateX(-${currentIndex * 100}%)`;
      prevButton.disabled = currentIndex === 0;
      nextButton.disabled = currentIndex === maxIndex;
    };

    const goToPrevious = () => {
      if (currentIndex <= 0) {
        return;
      }
      currentIndex -= 1;
      updateCarousel();
    };

    const goToNext = () => {
      if (currentIndex >= maxIndex) {
        return;
      }
      currentIndex += 1;
      updateCarousel();
    };

    prevButton.addEventListener("click", goToPrevious);
    nextButton.addEventListener("click", goToNext);

    viewport.addEventListener("keydown", (event) => {
      if (event.key === "ArrowLeft") {
        event.preventDefault();
        goToPrevious();
      } else if (event.key === "ArrowRight") {
        event.preventDefault();
        goToNext();
      }
    });

    let startX = 0;
    let pointerActive = false;

    viewport.addEventListener("pointerdown", (event) => {
      startX = event.clientX;
      pointerActive = true;
      viewport.setPointerCapture(event.pointerId);
    });

    viewport.addEventListener("pointerup", (event) => {
      if (!pointerActive) {
        return;
      }

      const delta = event.clientX - startX;
      const threshold = 40;

      if (delta < -threshold) {
        goToNext();
      } else if (delta > threshold) {
        goToPrevious();
      }

      pointerActive = false;
    });

    viewport.addEventListener("pointercancel", () => {
      pointerActive = false;
    });

    if (slides.length === 1) {
      prevButton.hidden = true;
      nextButton.hidden = true;
    }

    updateCarousel();
  });
}

function normalizeSearchValue(value) {
  return value
    .normalize("NFD")
    .replace(/[\u0300-\u036f]/g, "")
    .toLowerCase()
    .trim();
}

function isKnownUmaiaCourse(value) {
  const normalizedValue = normalizeSearchValue(value || "");
  return umaiaCourses.some((course) => normalizeSearchValue(`${course.title} ${course.cycle}`) === normalizedValue || normalizeSearchValue(course.title) === normalizedValue);
}

function getFilteredCourses(query) {
  const normalizedQuery = normalizeSearchValue(query || "");

  return umaiaCourses
    .map((course) => {
      const titleValue = normalizeSearchValue(course.title);
      const areaValue = normalizeSearchValue(course.area);
      const cycleValue = normalizeSearchValue(course.cycle);
      const searchable = `${titleValue} ${areaValue} ${cycleValue}`;

      let score = 3;

      if (!normalizedQuery) {
        score = 0;
      } else if (titleValue.startsWith(normalizedQuery)) {
        score = 0;
      } else if (titleValue.includes(normalizedQuery)) {
        score = 1;
      } else if (searchable.includes(normalizedQuery)) {
        score = 2;
      } else {
        return null;
      }

      return { ...course, score };
    })
    .filter(Boolean)
    .sort((a, b) => {
      if (a.score !== b.score) {
        return a.score - b.score;
      }

      if (courseCycleOrder[a.cycle] !== courseCycleOrder[b.cycle]) {
        return courseCycleOrder[a.cycle] - courseCycleOrder[b.cycle];
      }

      if (a.area !== b.area) {
        return a.area.localeCompare(b.area, "pt");
      }

      return a.title.localeCompare(b.title, "pt");
    });
}

function initCourseAutocomplete() {
  const input = document.querySelector("[data-course-autocomplete]");
  const panel = document.querySelector("[data-course-suggestions]");

  if (!input || !panel) {
    return;
  }

  let optionButtons = [];
  let activeIndex = -1;

  const closePanel = () => {
    panel.hidden = true;
    panel.innerHTML = "";
    optionButtons = [];
    activeIndex = -1;
    input.setAttribute("aria-expanded", "false");
  };

  const setActiveOption = (nextIndex) => {
    optionButtons.forEach((button, index) => {
      button.classList.toggle("is-active", index === nextIndex);
      button.setAttribute("aria-selected", index === nextIndex ? "true" : "false");
    });

    activeIndex = nextIndex;

    if (activeIndex >= 0) {
      optionButtons[activeIndex].scrollIntoView({ block: "nearest" });
    }
  };

  const selectCourse = (courseTitle) => {
    input.value = courseTitle;
    input.setCustomValidity("");
    closePanel();
  };

  const renderPanel = (query = input.value) => {
    const matches = getFilteredCourses(query);
    panel.innerHTML = "";

    if (!matches.length) {
      const emptyState = document.createElement("div");
      emptyState.className = "autocomplete-empty";
      emptyState.innerHTML = uiMessages[currentLanguage].courseNoResults;
      panel.appendChild(emptyState);
      panel.hidden = false;
      input.setAttribute("aria-expanded", "true");
      optionButtons = [];
      activeIndex = -1;
      return;
    }

    const groups = new Map();

    matches.forEach((course) => {
      const groupKey = course.cycle;
      if (!groups.has(groupKey)) {
        groups.set(groupKey, []);
      }
      groups.get(groupKey).push(course);
    });

    groups.forEach((courses, cycle) => {
      const group = document.createElement("div");
      group.className = "autocomplete-group";

      const label = document.createElement("div");
      label.className = "autocomplete-group-label";
      label.textContent = cycle;
      group.appendChild(label);

      courses.forEach((course) => {
        const button = document.createElement("button");
        button.type = "button";
        button.className = "autocomplete-option";
        button.setAttribute("role", "option");
        button.setAttribute("aria-selected", "false");

        const title = document.createElement("span");
        title.className = "autocomplete-option-title";
        title.textContent = course.title;

        const meta = document.createElement("span");
        meta.className = "autocomplete-option-meta";
        meta.textContent = `${course.cycle} - ${course.area}`;

        button.appendChild(title);
        button.appendChild(meta);
        button.addEventListener("click", () => selectCourse(course.title));
        group.appendChild(button);
      });

      panel.appendChild(group);
    });

    optionButtons = [...panel.querySelectorAll(".autocomplete-option")];
    activeIndex = -1;
    panel.hidden = false;
    input.setAttribute("aria-expanded", "true");
  };

  input.addEventListener("focus", () => {
    renderPanel(input.value);
  });

  input.addEventListener("input", () => {
    input.setCustomValidity("");
    renderPanel(input.value);
  });

  input.addEventListener("keydown", (event) => {
    if (panel.hidden && (event.key === "ArrowDown" || event.key === "ArrowUp")) {
      renderPanel(input.value);
    }

    if (!optionButtons.length) {
      if (event.key === "Escape") {
        closePanel();
      }
      return;
    }

    if (event.key === "ArrowDown") {
      event.preventDefault();
      const nextIndex = activeIndex >= optionButtons.length - 1 ? 0 : activeIndex + 1;
      setActiveOption(nextIndex);
      return;
    }

    if (event.key === "ArrowUp") {
      event.preventDefault();
      const nextIndex = activeIndex <= 0 ? optionButtons.length - 1 : activeIndex - 1;
      setActiveOption(nextIndex);
      return;
    }

    if (event.key === "Enter" && activeIndex >= 0) {
      event.preventDefault();
      optionButtons[activeIndex].click();
      return;
    }

    if (event.key === "Escape") {
      closePanel();
    }
  });

  document.addEventListener("click", (event) => {
    if (!event.target.closest(".autocomplete-field")) {
      closePanel();
    }
  });

  document.addEventListener("riseup:languagechange", () => {
    if (!panel.hidden) {
      renderPanel(input.value);
    }
  });

  const form = input.closest("form");
  if (form) {
    form.addEventListener("reset", () => {
      window.setTimeout(() => {
        input.setCustomValidity("");
        closePanel();
      }, 0);
    });
  }
}

function getSupabaseConfig() {
  const sourceConfig = window.RISEUP_SUPABASE || window.SUPABASE_CONFIG || window.SUPABASE || {};
  const resolvedPublicKey =
    typeof sourceConfig.publicKey === "string" ? sourceConfig.publicKey :
    typeof sourceConfig.anonKey === "string" ? sourceConfig.anonKey :
    typeof sourceConfig.publishableKey === "string" ? sourceConfig.publishableKey :
    typeof sourceConfig.key === "string" ? sourceConfig.key :
    "";

  return {
    url: typeof sourceConfig.url === "string" ? sourceConfig.url.trim().replace(/\/+$/, "") : "",
    publicKey: typeof resolvedPublicKey === "string" ? resolvedPublicKey.trim() : "",
    tables: typeof sourceConfig.tables === "object" && sourceConfig.tables !== null ? sourceConfig.tables : {}
  };
}

function isSupabaseConfigured(config) {
  if (!config.url || !config.publicKey) {
    return false;
  }

  return !/COLOCA_AQUI|SUPABASE_ANON_KEY|SUPABASE_PUBLISHABLE_KEY|YOUR_/i.test(config.publicKey);
}

async function readSupabaseError(response) {
  const responseText = await response.text();

  if (!responseText) {
    return `HTTP ${response.status}`;
  }

  try {
    const parsed = JSON.parse(responseText);
    return parsed.message || parsed.error_description || parsed.error || responseText;
  } catch (error) {
    return responseText;
  }
}

function setSubmitButtonState(button, isLoading) {
  if (!button) {
    return;
  }

  if (!button.dataset.defaultLabel) {
    button.dataset.defaultLabel = button.textContent.trim();
  }

  button.disabled = isLoading;
  button.classList.toggle("is-loading", isLoading);
  button.textContent = isLoading ? uiMessages[currentLanguage].sending : button.dataset.defaultLabel;
}

function getFormSubmissionPayload(form) {
  const basePayload = {
    source_page: document.body?.dataset.page || form.dataset.formType || "",
    page_url: window.location.href,
    language: currentLanguage,
    user_agent: navigator.userAgent || ""
  };

  if (form.dataset.formType === "application") {
    const ageValue = form.querySelector("[name=\"age\"]")?.value.trim() || "";

    return {
      ...basePayload,
      name: form.querySelector("[name=\"name\"]")?.value.trim() || "",
      email: form.querySelector("[name=\"email\"]")?.value.trim() || "",
      phone_contact: form.querySelector("[name=\"phone\"]")?.value.trim() || "",
      course: form.querySelector("[name=\"course\"]")?.value.trim() || "",
      study_year: form.querySelector("[name=\"study-year\"]")?.value.trim() || "",
      motivation: form.querySelector("[name=\"motivation\"]")?.value.trim() || "",
      linkedin: form.querySelector("[name=\"linkedin\"]")?.value.trim() || null,
      age: ageValue ? Number(ageValue) : null
    };
  }

  return {
    ...basePayload,
    name: form.querySelector("[name=\"name\"]")?.value.trim() || "",
    email: form.querySelector("[name=\"email\"]")?.value.trim() || "",
    message: form.querySelector("[name=\"message\"]")?.value.trim() || ""
  };
}

async function submitFormToSupabase(table, payload) {
  const config = getSupabaseConfig();

  if (!isSupabaseConfigured(config)) {
    throw new Error(uiMessages[currentLanguage].supabaseConfigError);
  }

  const response = await fetch(`${config.url}/rest/v1/${encodeURIComponent(table)}`, {
    method: "POST",
    headers: {
      apikey: config.publicKey,
      Authorization: `Bearer ${config.publicKey}`,
      "Content-Type": "application/json",
      Prefer: "return=minimal"
    },
    body: JSON.stringify(payload)
  });

  if (!response.ok) {
    throw new Error(await readSupabaseError(response));
  }
}

function initForms() {
  document.querySelectorAll("form[data-form-type]").forEach((form) => {
    form.addEventListener("submit", async (event) => {
      event.preventDefault();

      const statusElement = form.querySelector("[data-form-status]");
      const submitButton = form.querySelector("button[type=\"submit\"]");
      if (!statusElement) {
        return;
      }

      statusElement.textContent = "";
      statusElement.classList.remove("success", "error");

      const courseInput = form.querySelector("[data-course-autocomplete]");
      if (courseInput) {
        const isValidCourse = isKnownUmaiaCourse(courseInput.value);
        courseInput.setCustomValidity(courseInput.value.trim() && !isValidCourse ? uiMessages[currentLanguage].invalidCourse : "");
      }

      if (!form.checkValidity()) {
        statusElement.textContent = uiMessages[currentLanguage].validationError;
        statusElement.classList.add("error");
        form.reportValidity();
        return;
      }

      const config = getSupabaseConfig();
      const table = form.dataset.supabaseTable || config.tables?.[form.dataset.formType] || "";
      if (!table) {
        statusElement.textContent = uiMessages[currentLanguage].supabaseConfigError;
        statusElement.classList.add("error");
        return;
      }

      const successMessageKey = form.dataset.formType === "application" ? "applicationSuccess" : "contactSuccess";

      try {
        setSubmitButtonState(submitButton, true);
        await submitFormToSupabase(table, getFormSubmissionPayload(form));
        statusElement.textContent = uiMessages[currentLanguage][successMessageKey];
        statusElement.classList.add("success");
        form.reset();
      } catch (error) {
        console.error("Supabase form submission failed:", error);
        statusElement.textContent = error instanceof Error && error.message ? error.message : uiMessages[currentLanguage].submissionError;
        statusElement.classList.add("error");
      } finally {
        setSubmitButtonState(submitButton, false);
      }
    });
  });
}

