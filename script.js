function getTranslation(lang, key) {
  return translations[lang]?.[key] ?? null;
}

function capturePortugueseDefaults() {
  document.querySelectorAll("[data-i18n]").forEach((element) => {
    if (!element.dataset.ptContent) {
      const repairedContent = repairMojibake(element.innerHTML);
      if (repairedContent !== element.innerHTML) {
        element.innerHTML = repairedContent;
      }
      element.dataset.ptContent = repairedContent;
    }
  });

  document.querySelectorAll("[data-i18n-placeholder]").forEach((element) => {
    if (element.dataset.ptPlaceholder === undefined) {
      const placeholder = element.getAttribute("placeholder") || "";
      const repairedPlaceholder = repairMojibake(placeholder);
      if (repairedPlaceholder !== placeholder) {
        element.setAttribute("placeholder", repairedPlaceholder);
      }
      element.dataset.ptPlaceholder = repairedPlaceholder;
    }
  });

  document.querySelectorAll("[data-i18n-alt]").forEach((element) => {
    if (element.dataset.ptAlt === undefined) {
      const altText = element.getAttribute("alt") || "";
      const repairedAlt = repairMojibake(altText);
      if (repairedAlt !== altText) {
        element.setAttribute("alt", repairedAlt);
      }
      element.dataset.ptAlt = repairedAlt;
    }
  });
}

function repairDocumentMojibake() {
  if (!document.body) {
    return;
  }

  const walker = document.createTreeWalker(document.body, NodeFilter.SHOW_TEXT);
  const textNodes = [];

  while (walker.nextNode()) {
    if (mojibakePattern.test(walker.currentNode.nodeValue || "")) {
      textNodes.push(walker.currentNode);
    }
  }

  textNodes.forEach((node) => {
    node.nodeValue = repairMojibake(node.nodeValue);
  });

  document.querySelectorAll("[alt], [aria-label], [placeholder], [title]").forEach((element) => {
    ["alt", "aria-label", "placeholder", "title"].forEach((attribute) => {
      const currentValue = element.getAttribute(attribute);
      if (!currentValue) {
        return;
      }

      const repairedValue = repairMojibake(currentValue);
      if (repairedValue !== currentValue) {
        element.setAttribute(attribute, repairedValue);
      }
    });
  });

  document.title = repairMojibake(document.title);
}

function applyLanguage(lang) {
  currentLanguage = lang;
  document.documentElement.lang = lang;
  localStorage.setItem("riseUpLanguage", lang);

  document.querySelectorAll("[data-i18n]").forEach((element) => {
    const key = element.dataset.i18n;
    const translation = getTranslation(lang, key);
    if (translation !== null) {
      element.textContent = translation;
    } else {
      element.innerHTML = element.dataset.ptContent || element.innerHTML;
    }
  });

  document.querySelectorAll("[data-i18n-placeholder]").forEach((element) => {
    const key = element.dataset.i18nPlaceholder;
    const translation = getTranslation(lang, key);
    element.setAttribute("placeholder", translation !== null ? translation : element.dataset.ptPlaceholder || "");
  });

  document.querySelectorAll("[data-i18n-alt]").forEach((element) => {
    const key = element.dataset.i18nAlt;
    const translation = getTranslation(lang, key);
    element.setAttribute("alt", translation !== null ? translation : element.dataset.ptAlt || "");
  });

  document.querySelectorAll(".lang-switch button").forEach((button) => {
    button.classList.toggle("active", button.dataset.lang === lang);
  });

  const page = document.body.dataset.page;
  document.title = pageTitles[lang]?.[page] || document.title;

  const menuToggle = document.querySelector(".menu-toggle");
  if (menuToggle) {
    menuToggle.setAttribute("aria-label", document.body.classList.contains("nav-open") ? uiMessages[lang].menuClose : uiMessages[lang].menuOpen);
  }

  clearFormStatuses();
  document.dispatchEvent(new Event("riseup:languagechange"));
}

function clearFormStatuses() {
  document.querySelectorAll("[data-form-status]").forEach((statusElement) => {
    statusElement.textContent = "";
    statusElement.classList.remove("success", "error");
  });
}

